package BankModel;

public class Customer {
    private int id;
    private String tenCustomer;
    private int idNganHang;

    public String getTenCustomer() {
        return tenCustomer;
    }

    public void setTenCustomer(String tenCustomer) {
        this.tenCustomer = tenCustomer;
    }

    public int getIdNganHang() {
        return idNganHang;
    }

    public void setIdNganHang(int idNganHang) {
        this.idNganHang = idNganHang;
    }



    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
